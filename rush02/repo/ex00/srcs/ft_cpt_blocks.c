#include <ft.h>

int ft_cpt_blocks(char *str)
{
    int  i;
    
    i = 0;
    while (str[i])
        i++;
    if (i % 3 == 0)
        i = i / 3;
    else
        i = (i / 3) + 1;
    return (i);
}